select
    UCC.table_name, UCn.r_constraint_name,
    UCC.column_name, UCC.position
  from
    user_constraints UCn, user_cons_columns UCC
  where
    UCn.CONSTRAINT_TYPE = 'R'
    and UCn.table_name = UCC.table_name
    and UCn.constraint_name = UCC.constraint_name
